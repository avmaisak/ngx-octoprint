/**
 * 	Information regarding the size of the printed model
 */
export interface IDimensions {
  /**
   * The depth of the printed model, in mm
   */
  depth: number;
  /**
   * The height of the printed model, in mm
   */
  height: number;
  /**
   * The width of the printed model, in mm
   */
  width: number;
}
